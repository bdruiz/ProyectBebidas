    public void mostrar_datos(int num) {

        creditos = creditos + num;
        if (precio == 0) {
            Salidatexto.setText("Seleccione un producto.");
            Salidanumeros.setText("Cr:   " + creditos);
        } else {
            if (creditos >= precio) {
                Salidanumeros.setText("Cr: " + creditos + "  Cambio: " + (creditos - precio));
            } else {
                Salidanumeros.setText("Cr: " + creditos);
            }
        }
    }

